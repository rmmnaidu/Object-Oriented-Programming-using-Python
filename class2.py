# To create a class with only methods 
class wish:
    def mng():
        print('good morning')
    def aft():
        print('good afternoon')
    def evng():
        print('good evening')
    def ngt():
        print('good night') 
wish.mng() 
wish.aft() 
wish.evng() 
wish.ngt()    
  
'''
OUTPUT:
good morning
good afternoon
good evening
good night
'''
