# for loop using string
print("String Output:")
print("-------------")
for i in 'Python':
    print(i)

'''
Output:
P
y
t
h
o
n
'''

# for loop using list
print("List Output:")
print("-------------")
for lst in ['OOPP','DSA','CHEMISTRY','DEE']:
    print(lst)

'''
Output:
OOPP
DSA
CHEMISTRY
DEE
'''

# for loop using tuple
print("Tuple Output:")
print("-------------")
for tup in ('OOPP','DSA','CHEMISTRY','DEE'):
    print(tup)

'''
Output:
OOPP
DSA
CHEMISTRY
DEE
'''

# for loop using set
print("Set Output:")
print("-------------")
for set1 in {'OOPP','DSA','CHEMISTRY','DEE'}:
    print(set1)
for set2 in {10,20,30,-10,-20,-30}:
    print(set2)

'''
Output:
OOPP
DSA
CHEMISTRY
DEE
-30
10
-20
20
-10
30
'''

